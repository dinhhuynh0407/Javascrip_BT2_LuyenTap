// Khai báo biến dạng chuỗi
let strNumber = "42";

// Kiểm tra kiểu dữ liệu ban đầu
console.log("Trước khi chuyển đổi, typeof strNumber:", typeof strNumber);

// Chuyển đổi sang number bằng Number()
let numValue = Number(strNumber);

// Cộng thêm 8
let result = numValue + 8;

// In kết quả
console.log("Kết quả sau khi cộng:", result);

// Kiểm tra kiểu dữ liệu sau khi chuyển đổi
console.log("Sau khi chuyển đổi, typeof numValue:", typeof numValue);
