// Khai báo biến
let a = 15;
let b = 4;

// Tính toán
let sum = a + b;        // tổng
let difference = a - b; // hiệu
let product = a * b;    // tích
let quotient = a / b;   // thương
let remainder = a % b;  // dư số
let power = a ** b;     // lũy thừa (a^b)

// In ra console
console.log("Tổng: " + sum);
console.log("Hiệu: " + difference);
console.log("Tích: " + product);
console.log("Thương: " + quotient);
console.log("Dư số: " + remainder);
console.log("Lũy thừa: " + power);